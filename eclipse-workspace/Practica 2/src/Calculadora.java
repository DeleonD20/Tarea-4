public class Calculadora {

    public int sumar(int nu1, int nu2){

        int resultado;

        resultado = nu1 + nu2;

        return resultado;
    }

    public int restar(int nu1, int nu2){

        int resultado;

        resultado = nu1 - nu2;

        return resultado;
    }

    public int multiplicar(int nu1, int nu2){

        int resultado;

        resultado = nu1 * nu2;

        return resultado;
    }

    public int dividir(int nu1, int nu2){

        int resultado;

        resultado = nu1 / nu2;

        return resultado;
    }
}
